# ANSI color escape codes
_Z=$'\x1b[0m'
_R=$'\x1b[1;31m'
_G=$'\x1b[1;32m'
_C=$'\x1b[1;36m'


# Show a command and then run it
echodo() {
    builtin echo -e "${_C}TEST$ $@${_Z}"
    eval $@
    echo
}


good() {
	cat <<-:
	${_G}  ___              _                                   _    
	 / __|___  ___  __| |  __ ___ _ __  _ __  __ _ _ _  __| |___
	| (_ / _ \\/ _ \\/ _\` | / _/ _ \\ '  \\| '  \\/ _\` | ' \\/ _\` (_-<
	 \\___\\___/\\___/\\__,_| \\__\\___/_|_|_|_|_|_\\__,_|_||_\\__,_/__/
	${_Z}
	:
}


error() {
	cat <<-:
	${_R} ___                                                   _    
	| __|_ _ _ _ ___ _ _   __ ___ _ __  _ __  __ _ _ _  __| |___
	| _|| '_| '_/ _ \\ '_| / _/ _ \\ '  \\| '  \\/ _\` | ' \\/ _\` (_-<
	|___|_| |_| \\___/_|   \\__\\___/_|_|_|_|_|_\\__,_|_||_\\__,_/__/
	${_Z}
:
}

