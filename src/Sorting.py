from Usage import usage


def sort(args):
    if len(args) == 0:
        usage("Too few arguments", 'sort')

    for file in args:
        f = open(file)
        lines = f.readlines()
        sorted_lines = sorted(lines)
        for line in sorted_lines:
            print(line, end='')

        f.close()
